public class ArrayObjek {
    public static void main(String args[]) {
        int[] A = new int[] { 101, 102, 103, 104, 105 };
        System.out.println(A[1]);
        System.out.println(A.toString());
        for(int i=0; i<A.length; i++) {
            System.out.println(A[i]);
        }
    }
}

